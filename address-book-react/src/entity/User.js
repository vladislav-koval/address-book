class User {
    constructor(id, name, surname, lastName, street, home, apartment, number, category) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.lastName = lastName;
        this.street = street;
        this.home = home;
        this.apartment = apartment;
        this.number = number;
        this.category = category;
    }
}

export default User;